    public int[] generarNumerosAleatorios(int sumtotal, int divVotos) {
        Random rand = new Random();
        int[] votos = new int[divVotos];

        int[] restaCompl = new int[divVotos - 1];
        for (int i = 0; i < restaCompl.length; i++) {
            restaCompl[i] = rand.nextInt(sumtotal + 1);
        }
        java.util.Arrays.sort(restaCompl);

        votos[0] = restaCompl[0];
        for (int i = 1; i < restaCompl.length; i++) {
            votos[i] = restaCompl[i] - restaCompl[i - 1];
        }
        votos[divVotos - 1] = sumtotal - restaCompl[restaCompl.length - 1];

        return votos;
    }
    
