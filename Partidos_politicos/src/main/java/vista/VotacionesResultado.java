    public void llenadoDeCombos() {
        CandidatoDAO candidatoDAO = new CandidatoDAO();
        List<Candidato> candidatos = candidatoDAO.select();
        ComboBoxCandidatos1.addItem("Seleccione una opción");
        ComboBoxCandidatos2.addItem("Seleccione una opción");
        ComboBoxCandidatos3.addItem("Seleccione una opción");
        for (int i = 0; i < candidatos.size(); i++) {
            ComboBoxCandidatos1.addItem(candidatos.get(i).getNombreCandidato());
            ComboBoxCandidatos2.addItem(candidatos.get(i).getNombreCandidato());
            ComboBoxCandidatos3.addItem(candidatos.get(i).getNombreCandidato());
        }
    }
    
    public void llenadoDeTablas() {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("ID Candidato");
        modelo.addColumn("Nombre Candidato");
        modelo.addColumn("Primera Ronda");
        modelo.addColumn("Segunda Ronda");
        modelo.addColumn("Tercera Ronda");
        modelo.addColumn("Cuarta Ronda");
        modelo.addColumn("Quinta Ronda");
        modelo.addColumn("Votos Totales");
        RondaVotosDAO rondaVotosDAO = new RondaVotosDAO();
        List<RondaVotos> rondaVotos = rondaVotosDAO.select();
        tablaVotos.setModel(modelo);
        String[] dato = new String[8];
        for (int i = 0; i < rondaVotos.size(); i++) {
            dato[0] = Integer.toString(rondaVotos.get(i).getIdRondavotos());
            dato[1] = rondaVotos.get(i).getNombreCandidato();
            dato[2] = Integer.toString(rondaVotos.get(i).getRondaUno());
            dato[3] = Integer.toString(rondaVotos.get(i).getRondaDos());
            dato[4] = Integer.toString(rondaVotos.get(i).getRondaTres());
            dato[5] = Integer.toString(rondaVotos.get(i).getRondaCuatro());
            dato[6] = Integer.toString(rondaVotos.get(i).getRondaCinco());
            dato[7] = Integer.toString(rondaVotos.get(i).getRondaTotal());
            modelo.addRow(dato);
        }
    }
    
